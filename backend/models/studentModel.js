/*
 * Student model
*/

const mongoose = require("mongoose");

// define the blueprint of the student document for the db
const studentSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    course: {
      type: String,
      required: true,
    },
    enrollmentDate: {
      type: Date,
      required: true,
    },
    status: {
      type: String,
      enum: ["active", "inactive"],
      default: "active",
    },
  },
  {
    timestamps: true,
  }
);

// define and export a model for the course schema
module.exports = mongoose.model("Student", StudentSchema);
